const sitDownAtDrumSet = () => {
    console.log(`I sat down at my drumset.`)
}
const grabDrumSticks = () => {
    console.log(`I grabbed my drum sticks.`)
}
const hitTheDrums = () => {
    console.log(`I smacked the heck out of those drums.`)
}
const placeSticksDown = () => {
    console.log(`I placed the sticks down.`)
}
const exitTheDrumSet = () => {
    console.log(`I left the drumset.`)
}

const playDrums = () => {
    sitDownAtDrumSet()
    grabDrumSticks()
    hitTheDrums()
    placeSticksDown()
    exitTheDrumSet()
}
playDrums()